/**
 * Функция, которая принимает массив с числами
 * и находит сумму квадратов элементов этого массива.
 *
 * @param {number[]} arr - Массив с числами
 * @returns {number} - Сумма квадратов элементов массива
 */
function sumOfSquares(arr) {
    // Инициализируем переменную для хранения суммы квадратов
    let sum = 0;

    // Проходим по каждому элементу массива
    for (let i = 0; i < arr.length; i++) {
        // Возводим элемент в квадрат и добавляем к сумме
        sum += arr[i] * arr[i];
    }

    // Возвращаем полученную сумму
    return sum;
}

// Пример использования функции
const numbers = [1, 2, 3, 4, 5];
console.log(sumOfSquares(numbers)); // Вывод: 55
